<h1> About A Silly Place </h1>
<blockquote><strong>King Arthur: </strong> On second thought, let's not go to Camelot. It is a silly place. </blockquote>
<p>
	<strong>A Silly Place</strong> is a public blogg-ish website. You can say whatever silly thing is on your mind, and it'll be published here for people to gawk at. Actually, chances are, it won't be published anywhere visible at all, since you're probably just running this site on your localhost or something! 
</p>
<p>
	<strong>A Silly Place</strong> is written in <a href="http://php.net">PHP</a> using <a href="http://codeigniter.com/">Code Igniter</a> so that I could learn Code Igniter. 
</p>
